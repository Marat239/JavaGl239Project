package model;

public class Bounds
{
    public double MinX;
    public double MaxX;
    public double MinY;
    public double MaxY;

    public Bounds(double minX, double minY, double maxX, double maxY)
    {
        MinX = minX;
        MaxX = maxX;
        MinY = minY;
        MaxY = maxY;
    }
}
